package com.MbrCmpProject.plj;

public class PercentType {
	private int pYear;
	private float pAmount, pPercent;

	public int getpYear() {
		return pYear;
	}

	public void setpYear(int pYear) {
		this.pYear = pYear;
	}

	public float getpAmount() {
		return pAmount;
	}

	public void setpAmount(float pAmount) {
		this.pAmount = pAmount;
	}

	public float getpPercent() {
		return pPercent;
	}

	public void setpPercent(float pPercent) {
		this.pPercent = pPercent;
	}

}
